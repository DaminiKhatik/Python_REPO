a, b = 1, 2
import math
a1=math.sin(a)  # returns the sine of 'a' in radians # Out: 0.8414709848078965
print(a1)
a2=math.cosh(b)  # returns the inverse hyperbolic cosine of 'b' in radians # Out: 3.7621956910836314
print(a2)

a3=math.atan(math.pi)  # returns the arc tangent of 'pi' in radians # Out: 1.2626272556789115
print(a3)

a4=math.hypot(a, b) # returns the Euclidean norm, same as math.sqrt(a*a + b*b) # Out: 2.23606797749979
print(a4)

