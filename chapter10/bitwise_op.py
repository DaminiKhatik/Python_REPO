#Bitwise NOT
a=~2

print(a)

b=~9
print(b)

# Bitwise XOR (Exclusive OR)
a=bin(60 ^ 30)
print(a)

# Bitwise AND
a=bin(60 & 30)
print(a)

#Bitwise OR
a=60 | 30 
print(a)

#Bitwise Left Shift
a=2 << 2 
print(a)

# Bitwise Right Shift
a=8 >> 2 
print(a)

# Inplace Operations
a = 0b001
a &= 0b010 
print(a)